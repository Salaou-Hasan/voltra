#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn pack_unpack_roundtrip() {
        let ts = HlcTimestamp::pack(1_700_000_000_000, 42);
        assert_eq!(ts.wall_ms(), 1_700_000_000_000);
        assert_eq!(ts.logical(), 42);
    }

    #[test]
    fn ordering_wall_clock_dominates() {
        let a = HlcTimestamp::pack(100, 999);
        let b = HlcTimestamp::pack(101, 0);
        assert!(b > a, "higher wall clock must beat higher logical");
    }

    #[test]
    fn ordering_logical_breaks_ties() {
        let a = HlcTimestamp::pack(100, 5);
        let b = HlcTimestamp::pack(100, 6);
        assert!(b > a);
    }

    #[test]
    fn clock_now_is_monotonic() {
        let clock = HlcClock::new();
        let mut prev = clock.now();
        for _ in 0..1000 {
            let next = clock.now();
            assert!(next > prev, "clock must be strictly monotonic");
            prev = next;
        }
    }

    #[test]
    fn clock_receive_advances_past_remote() {
        let clock = HlcClock::new();
        // Simulate a remote timestamp far in the future.
        let far_future = HlcTimestamp::pack(9_999_999_999_999, 7);
        let after = clock.receive(far_future);
        assert!(after > far_future, "local clock must advance past the received timestamp");
    }

    #[test]
    fn clock_receive_idempotent_for_old_messages() {
        let clock = HlcClock::new();
        let t1 = clock.now();
        // Receiving a very old message must not roll the clock backward.
        let old = HlcTimestamp::pack(1, 0);
        let t2 = clock.receive(old);
        assert!(t2 > t1);
    }

    #[test]
    fn concurrent_now_calls_are_unique() {
        use std::collections::HashSet;
        use std::sync::Arc;
        let clock = Arc::new(HlcClock::new());
        let threads: Vec<_> = (0..8).map(|_| {
            let c = clock.clone();
            std::thread::spawn(move || {
                (0..1000).map(|_| c.now().raw()).collect::<Vec<_>>()
            })
        }).collect();
        let mut all: Vec<u64> = threads.into_iter().flat_map(|h| h.join().unwrap()).collect();
        let len = all.len();
        all.sort_unstable();
        all.dedup();
        assert_eq!(all.len(), len, "all HLC timestamps must be unique across threads");
    }

    #[test]
    fn zero_constant_is_zero() {
        assert_eq!(HlcTimestamp::ZERO.raw(), 0);
        assert_eq!(HlcTimestamp::ZERO.wall_ms(), 0);
        assert_eq!(HlcTimestamp::ZERO.logical(), 0);
    }
}
